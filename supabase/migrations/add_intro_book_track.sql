-- =====================================================================
-- 무료 입문서 기본 표시 트랙 컬럼 추가
--
-- 배경:
--   입문서가 두 종류(일반/호주)인데, 무료 신청자가 어느 책을 기본으로
--   볼지를 계정에 기억해둔다. book-request.html 신청 시 "호주/뉴질랜드
--   직접 제출 여부"로 설정되고, 내 대시보드의 "다른 버전 보기" 토글로
--   변경된다(최근 선택이 기본 = 시간 순서).
--
-- 값:
--   'regular'   = 일반 입문서 (기본값)
--   'australia' = 호주 입문서
--
-- 비고:
--   코드는 이 컬럼이 없어도 'regular'로 폴백하도록 작성돼 있어
--   마이그레이션 적용 전에도 서비스가 깨지지 않는다. 다만 호주 선택
--   저장/표시는 이 컬럼이 있어야 동작한다.
--
-- 2026-06-05
-- =====================================================================

ALTER TABLE users
ADD COLUMN IF NOT EXISTS intro_book_track TEXT DEFAULT 'regular';

COMMENT ON COLUMN users.intro_book_track IS '무료 입문서 기본 표시 트랙: regular(일반) 또는 australia(호주). book-request 신청 시 설정, 대시보드 토글로 변경.';
